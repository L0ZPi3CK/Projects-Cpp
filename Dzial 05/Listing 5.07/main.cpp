#include <iostream>



int main()
{

    using std::cout;
    using std::cin;
    using std::endl;

    int a = 20;
    int b = 20;

    cout << "a  = " << a << ":  b = " << b << "\n";
    cout << "a++ = " << a++ << ": ++b = " << ++b << "\n";
    cout << "a  = " << a << ":  b = " << b << "\n";


    return 0;
}
